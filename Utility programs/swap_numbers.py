# A program to swap numbers

first_number = int(input("Enter the first number ")) # 10

second_number = int(input("Enter the second number ")) # 20

temp = first_number + second_number # 30

first_number = temp - first_number # 20

second_number = temp - second_number

print("The first number is ", first_number)
print("The second number is ", second_number)

